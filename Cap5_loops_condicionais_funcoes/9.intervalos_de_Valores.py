#função range
#imprimindo numeros de 1 a 10
for i in range(1, 11):
   print(i)
#espaçamento
print('\n----\n')
#imprimindo numeros impares entre 0 e 20
#imprimindo numeros pares entre 50 e 101
for i in range(50, 101, 2):
   print(i)
#espaçamento
print('\n----\n')
#imprimindo os nuemros pares negativos entre 0 e -20
for i in range(0, -20, -2):
   print(i)
#espaçamento
print('\n----\n')   
#usando o tamanho de uma lista na função range()
lista = ['abacaxi', 'banana','morango','uva']
lista_tamanho = len(lista)
for i in range(lista_tamanho):
   print(lista[i])
#espaçamento
print('\n----\n')

