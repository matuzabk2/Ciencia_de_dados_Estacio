#criando uma lista
lista = [100, -2, 12, 65, 0]
print(type(lista))

#verificando metodos e atributos da lista
print(lista)

#usando um metodo do objeto lista
lista.append(100)

#imprimindo a lista
print(lista)

#usando um metodo do objeto lista
lista.count(100)

#a funçao help() explica como utilizar cada metodo de um objeto
print(dir(lista))
